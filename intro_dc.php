<h1>Benefit Cliffs Calculator</h1>

Use this tool to see how your benefits may change when your earnings change.<br/><br>
You will:<br><br>
1. Enter basic information about your household.<br/>
2. Enter how your earnings will change. You can change how many hours you work, or your hourly wage, or both.<br/>
3. Answer more questions about your household.<br/>
4. See how your resources, benefits, and expenses will change.<br/>
5. Find out what to do next.<br><br>
Note: This tool does not work for all households. It will not be accurate if your household includes any of these:<br/>
- People currently without housing<br/>
- People age 65 or older<br/> 
- People who receive Medicare or Social Security benefits<br/>
- Children who receive federal or state disability benefits<br/>
- Young adult students on SSI (Supplemental Security Income)<br/>
- More than 5 children<br/>
- More than 4 adults<br/>
- People convicted of a felony that may result in ineligibility for benefits<br/>
- Foster children<br/>
- Children only, with no adult household members<br/>
- Current or former military service members<br/>
- Someone whose spouse died in the last two years<br/>
- People who qualify for programs supporting American Indian populations<br>
- Two or more married couples living together<br>
- A married person whose spouse does not live in the household<br><br>
If your household has any of these, do not use this tool. Call the XXX office at xxx-xxx-xxxx to find out how your benefits may change or ask your case manager.<br>

